package com.kanvalkalra.colormemory.game.helper;


public class RowColumnSet {

    private int row;
    private int column;

    public RowColumnSet(int row, int column) {
        this.row = row;
        this.column = column;
    }

    public int getRow() {
        return row;
    }

    public int getColumn() {
        return column;
    }
}
